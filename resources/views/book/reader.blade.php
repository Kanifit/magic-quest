            А когда вышел, за ним уже крался Филин, растопырив крылья.<br>
            Но Ёжик его не заметил.<br>
            Он нёс Медвежонку малиновое варенье и смотрел в небо. «Звезда!» — вдруг остановился Ёжик.Филин чуть не налетел на него и тоже уставился в небо.<br>
            <br>
            «И в луже Звезда…» — сам себе сказал Ёжик, увидев её отражение.<br>
            <br>

            Филин тоже наклонился над лужей, но ничего, кроме себя, не увидел и, рассердившись, взрыхлил своей мохнатой лапой воду.<br>
            <br>
            А Ёжик уже глядел в тёмный старый колодец.<br>
            «Угу!» — крикнул Ёжик.<br>
