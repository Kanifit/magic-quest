            <br>
            И Филин, проходя мимо колодца, тоже крикнул: «Угу!..» и остановился, удивленный.<br>
            Колодец и ему ответил: «Угу-у-у-у-у!».<br>
            Филин взгромоздился на сруб колодца и снова крикнул: «Угу!».<br>
            И колодец в ответ, гулко: «Угу-у-у-у-у-у-у…».<br>
            Филин хихикнул и они заухали, прислушиваясь друг к другу — Филин и старый колодец. А Ёжик со своим узелком уже семенил дальше, сам про себя беседуя с Медвежонком:<br>

            «А я ему скажу: „А я тебе малиновое варенье принёс”.<br>
            А он мне скажет: „Вот и самовар простыл, надо бы веточек подбросить… этих… ну как их… можжевеловых!”<br>
            А я ему скажу… а я ему скажу… а я е-му ска-жу…».<br>
            И вдруг застыл. Прямо перед ним из тумана выплыла белая Лошадь.<br>
            <br>
            «А интересно, — подумал Ёжик, — если Лошадь ляжет спать, она захлебнётся в тумане?» И он стал медленно спускаться с горки, чтобы попасть в туман и посмотреть, как там внутри.«Вот, ничего не видно. И даже лапы не видно».<br>

            «Лошадь!» — позвал Ёжик.<br>
            Но Лошадь ничего не сказала.<br>
            <br>
            И тут на Ёжика обрушился сухой Лист.<br>
            Ёжик от испуга закрыл лапами глаза… а когда выглянул… из под Листа, таинственно покачивая своим домиком, уплыла в туман Улитка… Ёжик опасливо приподнял сухой Лист…<br>
            <br>
            И совсем рядом услышал дыхание:<br>
            «А-ха, а-ха!» — вздымая боками и раздуваясь до неба, задышал Слон. Или это был не Слон?<br>
            Ёжик аккуратно положил Лист на место и, пятясь, на цыпочках, ушёл в туман.<br>
            А из тумана выглянула большая, добрая голова Лошади. Голова вкусно пофыркивала и хрумтела травой.<br>

            «Фр-р-р!» — вздохнула лошадиная голова и сухой Лист, как живой, взметнулся и отполз в сторону.Где-то вдали зазвенело и вдруг — «В-з-з-з!» — над головой Ёжика прянула и мгновенно пропала Летучая Мышь.<br>
            Ёжик даже не успел перепугаться, а вокруг него затрепетала, засеребрилась легкая стайка ночных Бабочек-Подёнок.<br>
            <br>
            «Хе-хе-хе-хе-хе!» — передразнил Ёжик и даже пробежал следом за ними, подпрыгнул, замахал лапами, представив себя ночной Бабочкой, но вдруг замер, прижав узелок…<br>
            Из тумана, как из форточки, выглянул Филин, ухнул: «Угу! У-гу-гу-гу-гу-гу!..» и растворился в тумане.<br>

            «Псих», — подумал Ёжик, поднял сухую палку и, ощупывая ею туман, двинулся вперед.<br>
            Палка, как слепая, блуждала в темноте, пока не упёрлась во что-то твёрдое.«Тук-тук!» — постучал Ёжик.<br>
            Положил узелок и, перебирая по палке лапами, увидел перед собой дерево с огромным дуплом.<br>
            «А-га!» — осторожно выдохнул в дупло Ёжик.
