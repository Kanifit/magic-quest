            Он метнулся назад, обежал дерево, крутнулся на месте… Узелка не было. Дерево медленно заволокло туманом. Ёжик остался один.«Ё-ёж-и-и-и-и-к!» — будто с края земли донёсся чей-то крик.<br>
            Туман сгущался вокруг Ёжика.
            Ёжик сорвал травинку, на которой сидел Светлячок, и, высоко подняв её над головой, как со свечой, наклоняясь и вглядываясь себе под ноги, побрёл в тумане.<br>

            Деревья, как мачты, тонули во мгле. Светлячок — маленький зеленый маяк — еле-еле теплясь, покачивался в тумане, освещая дорогу.Но и он упал в траву и погас. Стало совсем темно.<br>
            Туман закружился вокруг Ёжика. И Ёжик побежал. Ему казалось, что из тумана за ним несётся огромная Улитка, Летучая Мышь, Филин… задышал Слон, обрушился Лист с дерева.<br>

            Ёжик упал в траву и закрыл лапами голову.Но тут из тумана появилась Собака. Она положила перед Ёжиком узелок, зевнула во всю собачью пасть и пропала в тумане.<br>

            Издалека донёсся голос Медвежонка:
            «Ё-ёжи-и-и-и-ик!».
            «О-го-го-го-го!» — рванулся на крик Ёжик, но — бул-тых! — упал в воду.<br>

            «Я — в реке, — похолодел от страха Ёжик и, немного погодя, решил, — пускай река сама несёт меня».<br>
            Он глубоко вздохнул и река понесла его вниз по течению.
            Высоко в небе горели звёзды, над ним проплыла голова Лошади, где-то далеко тоненько играла балалайка…<br>

            «Я совсем промок. Я скоро утону», — подумал Ёжик.Вдруг кто-то дотронулся до его задней лапы.<br>
            «Извините… — беззвучно спросил Кто-то, — кто Вы и как сюда попали?»
            «Я — Ёжик, — тоже беззвучно ответил Ёжик, — я упал в реку».

            «Тогда садитесь ко мне на спину, — беззвучно проговорил Кто-то, — я отвезу Вас на берег».<br>
            Ёжик сел на чью-то широкую скользкую спину и через минуту оказался на берегу.<br>

            «Спасибо!» — вслух сказал Ёжик.
            «Не за что!» — беззвучно сказал Кто-то, кого Ёжик даже не видел, и пропал в волнах.<br>

            Ёжик с узелком сидел на брёвнышке и смотрел прямо перед собой остановившимися глазами.<br>
            Послышались чьи-то торопливые шаги.«Ёжик! Где же ты был? — плюхнулся рядом запыхавшийся Медвежонок, — я звал, звал, а ты не откликался!..»<br>
            Ёжик ничего не сказал. Он только чуть скосил глаза в сторону Медвежонка…<br>
            «Я уже и самовар на крыльце раздул, креслице плетёное придвинул, чтобы удобнее звёзды считать было… вот, думаю, сейчас придёшь, сядем, чайку попьём, с малиновым вареньем, ты ведь малиновое варенье несёшь, да? А я и самовар раздул и веточек этих… как их?..»<br>
            «Мож-же-ве-ло-вых», — медленно подсказал Ёжик.«Можжевеловых! — Обрадовался Медвежонок, — чтобы дымок пах… И… и… и… и в… и в… ведь кто же, кроме тебя, звёзды-то считать будет?!»<br>

            Медвежонок говорил, говорил, а Ёжик думал:<br>
            «Всё-таки хорошо, что мы снова вместе».<br>
